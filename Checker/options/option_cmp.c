#include "../HEADER/checker.h"

int	option_cmp(char *a, char *b)
{
	while (*a && *b && *a != '=' && *b != '=' && *a == *b)
	{
		a++;
		b++;
	}
	if (*a != *b)
		return (0);
	return (1);
}
